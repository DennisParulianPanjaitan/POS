    <h1 style="font-family: 'NamaFont', sans-serif;">=== HOME CARE ===</h1>
    <h2>Cleaning Tips</h2>
    <ul>
        <li>Rutin membersihkan permukaan meja dan furnitur di rumah.</li>
        <li>Bersihkan lantai secara teratur untuk menjaga kebersihan.</li>
        <li>Pastikan dapur dan kamar mandi tetap bersih dan higienis.</li>
        <li>Buang sampah secara teratur untuk mencegah penumpukan sampah.</li>
        <li>Cuci pakaian dan linen secara teratur.</li>
    </ul>

    <h2>Organization Tips</h2>
    <ul>
        <li>Atur barang-barang di rumah agar mudah ditemukan.</li>
        <li>Gunakan wadah penyimpanan untuk mengorganisir barang kecil.</li>
        <li>Sortir barang-barang yang tidak lagi diperlukan dan buang yang tidak terpakai.</li>
        <li>Buat jadwal rutin untuk membersihkan dan mengorganisir ruangan.</li>
        <li>Pertimbangkan untuk menyusun ulang furnitur jika diperlukan untuk optimalisasi ruang.</li>
    </ul>

    <h2>Health and Safety Tips</h2>
    <ul>
        <li>Pastikan rumah terbebas dari bahaya potensial, seperti kabel listrik yang berantakan.</li>
        <li>Pasang peralatan keamanan, seperti detektor asap dan CO2.</li>
        <li>Rutin memeriksa dan memastikan pintu dan jendela dalam keadaan baik.</li>
        <li>Simpan obat-obatan dan bahan berbahaya di tempat yang aman dan terkunci.</li>
        <li>Periksa peralatan rumah tangga secara berkala untuk memastikan keamanan.</li>
    </ul>

