    <h2>Baby Care Tips</h2>
    <ul>
        <li>Mandikan bayi dengan lembut menggunakan produk yang aman dan sesuai untuk kulit bayi.</li>
        <li>Gunakan popok yang nyaman dan rutin ganti popok untuk menjaga kebersihan.</li>
        <li>Beri bayi susu atau makanan sesuai dengan usianya.</li>
        <li>Pastikan ruangan tempat bayi tidur aman dan bebas benda berbahaya.</li>
        <li>Perhatikan tanda-tanda kesehatan bayi dan konsultasikan dengan dokter jika perlu.</li>
    </ul>

    <h2>Kid Care Tips</h2>
    <ul>
        <li>Promosikan pola makan sehat dengan memasukkan buah, sayur, dan makanan bergizi lainnya.</li>
        <li>Dorong anak untuk beraktivitas fisik dan bermain di luar ruangan.</li>
        <li>Bantu anak untuk menjaga kebersihan diri dan membiasakan mandi secara teratur.</li>
        <li>Pastikan anak memiliki waktu istirahat yang cukup dan tidur nyenyak di malam hari.</li>
        <li>Libatkan anak dalam kegiatan kreatif dan pendidikan untuk pengembangan positif.</li>
    </ul>

